import Foundation

func metersToFeet(meters: Double) -> Double {

    meters * 3.28084

}

func metersPerSecondToMPH(pace: Double) -> Double {

    pace * 2.23694

}

func metersPerSecondToKPH(pace: Double) -> Double {

    pace * 3.6

}
